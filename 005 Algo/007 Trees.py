#--------------------TREES AND ADT(Abstract Data Types)--------------------
# graphically, trees have a root at the top and branches and leaves from top to bottom
# a file system is a  classical example of a tree
# A node is a fundamental part of a tree. It has a named ofter referred as key.
# it also has a payload which may or may not be useful depending the application.
# an edge connects two nodes to show that there is a relationship between them
# (edges are arrows going from node to node)
# a path is an ordered list of nodes connected by edges
# children nodes are the ones with incoming edges from a parent node
# a leaf is a node that has no children (files are leaves)
# the level of a node is the number of edges from the root node
# the height of a tree is equal to the maximum level of any node in the tree.

